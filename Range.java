package ba.bitcamp.medina.banjic;

public class Range {

	public static void main(String[] args) {


		float number= 2.6F;
		
		// I declared a variable float to see if it is in a given range of numbers or not
		
		if (number >= 5 && number <= 7.5){
			
			System.out.println("The number is between 5 and 7.5.");
		}
		
		else if (number > 7.5 && number <= 10){
			
			System.out.println("The number is between 7.5 and 10.");
		}
		
		else 
			
			System.out.println("The number is out of a range.");
		
		// I made an IF statement to see if the given number is in or out of the given range

	}

}
